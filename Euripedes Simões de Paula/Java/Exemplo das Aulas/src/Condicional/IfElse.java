package Condicional;

public class IfElse {

	public static void main(String[] args) {
		
		int x = 10;

        if (x > 0) {
            System.out.println("x é positivo");
        } else if (x < 0) {
            System.out.println("x é negativo");
        } else {
            System.out.println("x é zero");
        }
		
	}
	
}
