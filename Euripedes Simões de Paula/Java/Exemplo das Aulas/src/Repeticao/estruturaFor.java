package Repeticao;

public class estruturaFor {
	
	public static void main(String[] args) {
		
		// Conatndo com Incremento
		for(int a = 0; a <= 10; ++a) { 
			System.out.println("A variável A somou: " + a);
		}
		
		// Comntando com Decremento
		for(int b = 10; b >= 0; --b) {
			System.out.println("A variável B subtraiu : " + b);
		}
		
	}
	
}
