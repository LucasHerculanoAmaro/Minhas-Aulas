package Operadores;

public class OperadoresString {
	
	public static void main(String[] args) {
		
		String soberano = "Tricolo";
		String timao = "Corinthians";
		
		System.out.println(soberano + timao);
		System.out.println(timao + soberano);
		System.out.println(soberano + " é maior do que " + timao);
		System.out.println("Quem nasceu " + timao + " nunca será maior do que " + soberano);
		
	}

}
